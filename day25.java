
public class day25 {
    public static void main(String[] args) {
        int a = 8;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a+"\n");
        int b = 12;
        System.out.println(b);
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
    }
}
